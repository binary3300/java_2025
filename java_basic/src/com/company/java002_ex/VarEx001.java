package com.company.java002_ex;

public class VarEx001 {
	public static void main(String[]arg) {
		//선생님의 답은 int apple=0; 으로 시작해서 apple=1000;으로 값주기
		int apple =1000;
		System.out.println("사과 가격은 " +apple+"원 입니다.");
		System.out.printf("사과가격은 %d원입니다.\n", apple);
		int apple2 =4000;                                                               
		System.out.println("사과 가격은 " +apple2+"원 입니다.");
	}
} 
